package com.example.myapplication.ui.net.model

import java.util.Date

class ExperienceInfoBrief {
    private val companyName: String? = null

    /**
     * 职业名称
     * designation(position) in this company, for example java programmer
     */
    private val designation: String? = null

    /**
     * 开始时间
     * the date of starting working in this company
     */
    private val startTime: Date? = null

    /**
     * the date of end working in this company
     * 开始时间结束时间
     */
    private val endTime: Date? = null
}