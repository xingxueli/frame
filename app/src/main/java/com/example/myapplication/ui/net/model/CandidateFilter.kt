package com.example.myapplication.ui.net.model

class CandidateFilter {

    /**
     * Availability Status
     */
    private val status: List<Int>? = null

    /**
     * Required education
     */
    private val education: List<Int>? = null

    /**
     * Required experience
     */
    private val experience: List<Int>? = null

    /**
     * Offered salary (Choose one option)
     */
    private val salary: List<Int>? = null

    /**
     * gender 1：man  2：women  0:unselect
     */
    private val gender: Int? = null

    /**
     * Minimum age range selected by the user
     */
    private val minAge: Int? = null

    /**
     * Maximum age range selected by the user
     */
    private val maxAge: Int? = null
}